SELECT A.IP ,C.SENSOR_ID ,C.SENSOR_VALUES\
FROM PLANT A  ,SENSOR_TRANSECTION C
LEFT JOIN  SENSOR_TRANSECTION C ON C.PLANT_ID  = A.ID
LEFT JOIN  SENSOR_TRANSECTION C ON C.SENSOR_ID = A.SENSOR_ID
WHERE B.LAST_UPDATE BETWEEN '2017-01-01 12:00:00' AND '2017-09-21 20:00:00'
AND A.IP = "192.168.0.1";

SELECT A.IP, B.SENSOR_ID, B.SENSOR_VALUES FROM PLANT A 
LEFT JOIN SENSOR_TRANSECTION B ON 
B.PLANT_ID = A.ID WHERE B.LAST_UPDATE BETWEEN '2017-01-01 00:00:00' AND '2017-02-01 00:00:00'

SELECT C.IP, C.SENSOR_ID, D.SENSOR_NAME, C.SENSOR_VALUES 
FROM SENSOR D 
LEFT JOIN 
(SELECT A.IP, B.SENSOR_ID, B.SENSOR_VALUES 
FROM PLANT A 
LEFT JOIN SENSOR_TRANSECTION B ON B.PLANT_ID = A.ID 
WHERE B.LAST_UPDATE BETWEEN '2017-01-01 00:00:00' AND '2017-02-01 00:00:00') 
C ON C.SENSOR_ID = D.SENSOR_ID